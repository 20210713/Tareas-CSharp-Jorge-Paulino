using System;

namespace Otros_Tipos_de_datos
{
    class Program
    {
        static void Main(string[] args)
        {
            //Entero sin signo, 0 a 255
            byte a;

            //Entero con signo, -128 a 127
            sbyte b;

            //Entero con signo, -32.768 a 32.767
            short c;

            //Entero sin signo, 0 a 65535
            ushort d;

            //Tipo base de todos los otros tipos
            object e;

            //Entero con signo, -922337203685477508 a 922337203685477507
            long f;

            //Entero sin signo, 0 a 18446744073709551615
            ulong g;
        }
    }
}
